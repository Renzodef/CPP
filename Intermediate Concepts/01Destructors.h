// The header file (.h) holds the function declarations (prototypes) and variable declarations.
// The implementation of the class and its methods go into the source file (.cpp).
class MyClass
{
public:
    MyClass();
    // The name of a destructor will be exactly the same as the class, 
    // only prefixed with a tilde (~). 
    // A destructor can't return a value or take any parameters.
    ~MyClass();
};